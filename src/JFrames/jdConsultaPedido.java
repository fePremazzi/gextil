    private void btnSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelecionarActionPerformed
        int row = jTable1.getSelectedRow();
        if (row != -1) {
            int value = (int) jTable1.getModel().getValueAt(row, 0);
            PedidoVO ped = pedCont.getById(value);
            pedidoSelected.setTipo(ped.getTipo());
            pedidoSelected.setDataEmissao(ped.getDataEmissao());
            pedidoSelected.setDataEntrega(ped.getDataEntrega());
            pedidoSelected.setNumPedido(ped.getNumPedido());
            pedidoSelected.setCliente(ped.getCliente());
            pedidoSelected.setUsuario(ped.getUsuario());
            pedidoSelected.setOrcamento(ped.getOrcamento());
            pedidoSelected.setId(ped.getId());
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um item para continuar.");
        }
    }//GEN-LAST:event_btnSelecionarActionPerformed

    private void fillTable() {
        List<PedidoVO> lista = pedCont.getAll();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        for (PedidoVO ped : lista) {
            Object[] linha = {ped.getId(),
                ped.getTipo().toString(),
                ped.getDataEmissao().toString(),
                ped.getDataEntrega().toString(),
                ped.getNumPedido(),
                clCont.getById(ped.getCliente()).getNome(),
                usrCont.getById(ped.getUsuario()).getUsername(),
                ped.getOrcamento()
            }; //alguma linha
            model.addRow(linha);
        }
    }

